// Keywords are matched against a product's category and sub-category names.
// Longer, more specific keywords are checked first so "kada" never falls
// through to "bangle" and "bracelet" never matches on a stray "ban".
const CATEGORY_KEYWORDS = [
  ['kada', 'kada'],
  ['kadaa', 'kada'],
  ['bangle', 'bangle'],
  ['bracelet', 'bracelet'],
  ['anklet', 'anklet'],
  ['payal', 'anklet'],
  ['necklace', 'necklace'],
  ['mangalsutra', 'necklace'],
  ['pendant', 'necklace'],
  ['chain', 'necklace'],
  ['ring', 'ring'],
];

/**
 * Resolves the size chart that applies to a product, or null when the style is
 * not sized (earrings, nose pins, brooches and the like).
 */
// Styles that are never sized but whose names collide with a sizing keyword —
// "Ear Rings" must not resolve to the ring chart.
const NEVER_SIZED = [/\bear\s*rings?\b/, /\bnose\s*(pin|ring)s?\b/, /\btoe\s*rings?\b/];

export function resolveSizeChart({ category, subCategory } = {}) {
  const haystack = `${subCategory || ''} ${category || ''}`.toLowerCase();

  if (NEVER_SIZED.some((pattern) => pattern.test(haystack))) {
    return null;
  }

  for (const [keyword, chartKey] of CATEGORY_KEYWORDS) {
    // Whole words only, with an optional plural — a substring match would put
    // "Earrings" onto the ring chart.
    if (new RegExp(`\\b${keyword}(s|es)?\\b`).test(haystack)) {
      return SIZE_CHARTS[chartKey];
    }
  }

  return null;
}

/** Every selectable size for a product, or [] when the style is not sized. */
export function sizeOptionsFor(product) {
  return resolveSizeChart(product)?.rows || [];
}

/** True when `size` is a valid selection for the product's chart. */
export function isValidSize(product, size) {
  const chart = resolveSizeChart(product);
  if (!chart) return !size;
  return chart.rows.some((row) => row.size === size);
}

export function defaultSizeFor(product) {
  const rows = sizeOptionsFor(product);
  if (!rows.length) return '';
  // Mid-range sizes are the most commonly ordered, so open on one.
  return rows[Math.floor(rows.length / 2)].size;
}
